import math as myMath

# ceil 向上取整
print(myMath.ceil(20))      # 20
print(myMath.ceil(20.002))  # 21

# floor 向下取整
print(myMath.floor(20))     # 20
print(myMath.ceil(20.002))  # 21

# sqrt 开平方
print(myMath.sqrt(81))  # 9.0

# 对数
print(myMath.log(100, 2))   # 6.643856189774725
print(2 ** (myMath.log(100, 2)))   # 100.00000000000004

